package ru.imatveev.library.domain.entity;

import io.swagger.v3.oas.annotations.media.Schema;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import java.util.UUID;

@Data
@Entity
@Table(name = "meta_info")
@AllArgsConstructor
@NoArgsConstructor
@Builder
@Schema(description = "Info about file")
public class MetaInfo {
    @Id
    @Schema(description = "Unique identifier, generated by server", example = "9c805a85-46c1-4b73-9367-5eebd0c1ae5d")
    private UUID id;
    @Column(name = "file_name")
    @Schema(name = "File name", example = "Kubernetes for kids.pdf")
    private String fileName;
}
